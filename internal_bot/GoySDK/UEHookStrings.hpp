inline std::string dec(const std::uint8_t* p, std::size_t n, std::uint8_t k = 0x5A) {
    std::string s(n, '\0');
    for (std::size_t i = 0; i < n; ++i)
        s[i] = static_cast<char>(p[i] ^ k);
    return s;
}

